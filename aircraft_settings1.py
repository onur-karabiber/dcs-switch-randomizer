def generate_lua(metadata: dict) -> str:
    lines = []
    lines.append(f'-- Generated by DCS Switch Randomizer (DSR)')
    lines.append(f'-- Aircraft: {metadata["display_name"]}')
    lines.append(f'-- DO NOT EDIT MANUALLY — use the Settings dialog\n')
    lines.append(f'CR.register("{metadata["aircraft"]}", {{')
    lines.append('')

    CONTINUOUS_VALS = "{0, 0.25, 0.5, 0.75, 1.0}"

    for ctrl in metadata["controls"]:
        enabled = ctrl.get("enabled", True)
        t       = ctrl["type"]
        label   = ctrl["label"]
        dev     = ctrl["dev"]
        cmd     = ctrl.get("cmd")
        comment = f"    -- {label}"

        if t == "run":
            run_note = ctrl.get("run_note", "")
            lua_body = ctrl.get("lua_body", "")
            cmd_str  = str(cmd) if cmd is not None else "nil"
            if not enabled:
                lines.append(f"    -- [DISABLED] {label} (dev={dev}, cmd={cmd_str})")
                if run_note:
                    lines.append(f"    -- {run_note}")
            else:
                lines.append(comment)
                if run_note:
                    lines.append(f"    -- {run_note}")
                lines.append(f"    {{")
                lines.append(f"        dev={dev}, cmd={cmd_str}, label=\"{label}\",")
                lines.append(f"        run=function(device)")
                if lua_body:
                    for body_line in lua_body.splitlines():
                        lines.append(body_line)
                else:
                    lines.append(f"            -- [lua_body tanımsız]")
                lines.append(f"        end")
                lines.append(f"    }},")
            lines.append('')

        elif t == "continuous":
            if not enabled:
                lines.append(
                    f"    -- [DISABLED] {{ dev={dev}, cmd={cmd}, "
                    f"vals={CONTINUOUS_VALS}, label=\"{label}\" }},")
            else:
                lines.append(comment)
                lines.append(
                    f"    {{ dev={dev}, cmd={cmd}, "
                    f"vals={CONTINUOUS_VALS}, label=\"{label}\" }},")
            lines.append('')

        elif t == "select_one":
            # Radio-group: each position has its own cmd.
            # Build a weighted vals list of cmd indices, then emit a run block.
            positions = ctrl.get("positions", [])
            if not enabled:
                lines.append(f"    -- [DISABLED] {label} (select_one, dev={dev})")
            else:
                lines.append(comment)
                pos_comment = " / ".join(
                    f"{p['label']}={p['weight']}%{'(default)' if p.get('is_default') else ''}"
                    for p in positions
                )
                lines.append(f"    -- {pos_comment}")
                # Build weighted cmd list
                from math import gcd
                from functools import reduce
                from fractions import Fraction
                fracs = [Fraction(p["weight"]).limit_denominator(10000) for p in positions]
                denoms = [fr.denominator for fr in fracs]
                lcm_d = reduce(lambda a, b: a * b // gcd(a, b), denoms)
                ints  = [int(fr * lcm_d) for fr in fracs]
                g     = reduce(gcd, ints)
                weighted_cmds = []
                for i, p in enumerate(positions):
                    weighted_cmds += [p["cmd"]] * (ints[i] // g)
                cmds_str = "{" + ", ".join(str(c) for c in weighted_cmds) + "}"
                lines.append(f"    {{")
                lines.append(f"        dev={dev}, cmd=nil, label=\"{label}\",")
                lines.append(f"        run=function(device)")
                lines.append(f"            local cmds = {cmds_str}")
                lines.append(f"            device:performClickableAction(cmds[math.random(#cmds)], 1)")
                lines.append(f"        end")
                lines.append(f"    }},")
            lines.append('')

        elif t == "discrete":
            positions = ctrl.get("positions", [])
            vals = weights_to_vals(positions)
            vals_str = "{" + ", ".join(format_delta(v) for v in vals) + "}"
            pos_comment = " / ".join(
                f"{p['label']}={p['weight']}%{'(default)' if p.get('is_default') else ''}"
                for p in positions
            )
            if not enabled:
                lines.append(
                    f"    -- [DISABLED] {{ dev={dev}, cmd={cmd}, "
                    f"vals=..., label=\"{label}\" }},")
                lines.append(f"    -- {pos_comment}")
            else:
                lines.append(comment)
                lines.append(f"    -- {pos_comment}")
                lines.append(
                    f"    {{ dev={dev}, cmd={cmd}, "
                    f"vals={vals_str}, label=\"{label}\" }},")
            lines.append('')

    lines.append(f'}}, {metadata.get("delay", 3.0)})')
    lines.append('')
    return "\n".join(lines)


# ── RepeatButton ──────────────────────────────────────────────────────────────

class RepeatButton(QPushButton):
    INITIAL_DELAY  = 400
    START_INTERVAL = 80
    MIN_INTERVAL   = 25
    ACCEL_STEP     = 4

    def __init__(self, text: str, callback, **kwargs):
        super().__init__(text, **kwargs)
        self._callback   = callback
        self._locked     = False
        self._timer      = QTimer(self)
        self._timer.setSingleShot(False)
        self._timer.timeout.connect(self._on_tick)
        self._interval   = self.START_INTERVAL
        self._hold_steps = 0

    def set_locked(self, locked: bool, style_locked: str, style_normal: str):
        self._locked = locked
        self.setStyleSheet(style_locked if locked else style_normal)
        if locked:
            self._timer.stop()

    def mousePressEvent(self, e):
        if e.button() == Qt.LeftButton and not self._locked:
            self._callback()
            self._interval   = self.START_INTERVAL
            self._hold_steps = 0
            self._timer.start(self.INITIAL_DELAY)
        super().mousePressEvent(e)

    def mouseReleaseEvent(self, e):
        self._timer.stop()
        super().mouseReleaseEvent(e)

    def _on_tick(self):
        if self._locked:
            self._timer.stop()
            return
        if self._timer.interval() == self.INITIAL_DELAY:
            self._timer.setInterval(self._interval)
        self._callback()
        self._hold_steps += 1
        new_interval = max(self.MIN_INTERVAL, self._interval - self.ACCEL_STEP)
        if new_interval != self._interval:
            self._interval = new_interval
            self._timer.setInterval(self._interval)


