class ControlCard(QFrame):
    def __init__(self, ctrl: dict, change_callback=None):
        super().__init__()
        self._ctrl            = ctrl
        self._pos_rows: list[PositionRow] = []
        self._change_callback = change_callback

        self.setObjectName("panel")
        self.setStyleSheet(f"QFrame#panel {{ background: rgba(34, 34, 34, 77); border-radius: 6px; }}")

        outer = QVBoxLayout(self)
        outer.setContentsMargins(10, 8, 6, 8)
        outer.setSpacing(4)

        top = QHBoxLayout()
        top.setSpacing(8)

        self.chk = QCheckBox()
        self.chk.setChecked(ctrl.get("enabled", True))
        self.chk.setStyleSheet(f"""
            QCheckBox::indicator {{
                width: 18px; height: 18px;
                border: 2px solid {MUTED};
                border-radius: 3px;
                background: {DARK};
            }}
            QCheckBox::indicator:checked {{
                background: {ACC};
                border: 2px solid {ACC};
                image: none;
            }}
            QCheckBox::indicator:unchecked {{
                background: #1a0000;
                border: 2px solid #c0392b;
                image: none;
            }}
        """)
        self.chk.stateChanged.connect(self._on_toggle)
        top.addWidget(self.chk)

        lbl = QLabel(abbreviate_label(ctrl["label"]))
        lbl.setStyleSheet(f"color: {FG}; font-size: 12pt; "
                           f"font-family: Consolas; font-weight: bold; background: transparent;")
        top.addWidget(lbl)
        top.addStretch()

        t = ctrl["type"]
        badge = QLabel(TYPE_LABEL.get(t, t.upper()))
        badge.setStyleSheet(
            f"background: {TYPE_COLOR.get(t, ACC)}; color: #111111; "
            f"font-size: 9pt; font-family: Consolas; font-weight: bold; "
            f"border-radius: 3px; padding: 2px 6px;")
        top.addWidget(badge)

        outer.addLayout(top)

        self._body = QVBoxLayout()
        self._body.setContentsMargins(26, 0, 0, 0)
        self._body.setSpacing(2)
        outer.addLayout(self._body)

        if t == "discrete":
            self._build_discrete()
        elif t == "select_one":
            self._build_select_one()

        self._set_body_enabled(ctrl.get("enabled", True))

    def _build_discrete(self):
        positions = self._ctrl.get("positions", [])
        non_defaults = [p for p in positions if not p.get("is_default", False)]
        defaults     = [p for p in positions if p.get("is_default", False)]
        for pos in non_defaults + defaults:
            is_def = pos.get("is_default", False)
            row = PositionRow(pos, self._pos_rows, is_def,
                              change_callback=self._change_callback)
            self._pos_rows.append(row)
            self._body.addWidget(row)

    def _build_select_one(self):
        # Same visual as discrete — positions shown with weights.
        # Default position is last (bottom), non-defaults first.
        positions = self._ctrl.get("positions", [])
        non_defaults = [p for p in positions if not p.get("is_default", False)]
        defaults     = [p for p in positions if p.get("is_default", False)]
        for pos in non_defaults + defaults:
            is_def = pos.get("is_default", False)
            row = PositionRow(pos, self._pos_rows, is_def,
                              change_callback=self._change_callback)
            self._pos_rows.append(row)
            self._body.addWidget(row)

    def _on_toggle(self, state):
        enabled = bool(state)
        self._ctrl["enabled"] = enabled
        self._set_body_enabled(enabled)
        if self._change_callback is not None:
            self._change_callback()

    def _set_body_enabled(self, enabled: bool):
        for i in range(self._body.count()):
            item = self._body.itemAt(i)
            if item and item.widget():
                item.widget().setEnabled(enabled)
                item.widget().setStyleSheet(item.widget().styleSheet())

    def is_valid(self) -> tuple[bool, str]:
        if self._ctrl["type"] not in ("discrete", "select_one") or not self._ctrl.get("enabled", True):
            return True, ""
        total = sum(r.get_weight() for r in self._pos_rows)
        if abs(total - 100) > 1:
            return False, self._ctrl["label"]
        return True, ""

    def flush_to_data(self):
        if self._ctrl["type"] in ("discrete", "select_one"):
            positions = self._ctrl["positions"]
            # _pos_rows are ordered non-defaults first, then defaults.
            # Match by label to avoid index mismatch.
            label_to_weight = {r._pos["label"]: r.get_weight() for r in self._pos_rows}
            for p in positions:
                if p["label"] in label_to_weight:
                    p["weight"] = label_to_weight[p["label"]]


